package MatrixPackage;

public class MatrixEle {
    private Integer x;
    private Integer y;
    public MatrixEle(Integer newX, Integer newY){
        x = newX;
        y = newY;
    }

    public Integer getX() {
        return x;
    }
    public void setX(Integer x) {
        this.x = x;
    }
    public Integer getY() {
        return y;
    }
    public void setY(Integer y) {
        this.y = y;
    }

}
